console.log('1. Arithmetic Operators');
 

let x;

x = 5 + 5; console.log(x);
x = 5 - 5; console.log(x);
x = 5 * 5; console.log(x);
x = 5 / 5; console.log(x);
x = 7 % 5; console.log(x);
console.log('--------------------------');

// Concatenation
x = 'Hello' + ' ' + 'World';
console.log(x);
console.log('--------------------------');

// Exponent
x = 2 ** 3;
console.log(x);
console.log('--------------------------');

// Increment
x = 1;
// x = x + 1;
x++;
console.log(x);
console.log('--------------------------');

// Decrement
// x = x - 1;
x--;
console.log(x);
console.log('2. Assignment Operators');

x = 10;

x += 5; console.log(x);
x -= 5; console.log(x);
x *= 5; console.log(x);
x /= 5; console.log(x);
x %= 5; console.log(x);
x **= 5; console.log(x);
console.log('3. Comparison Operators');

// Equal to (Just the value, not the type)
x = 2 == '2'; console.log(x);

// Equal to (Type and value)
x = 2 === '2'; console.log(x);

// Not equal to (Just the value, not the type)
x = 2 != '2'; console.log(x);

// Not equal to (Type and value)
x = 2 !== 2; console.log(x);
console.log('--------------------------');

// Greater than and less than
x = 10 > 5; console.log(x);
x = 10 < 5; console.log(x);
x = 10 <= 5; console.log(x);
x = 10 >= 5; console.log(x);

